function add(a,n){
    return a+n;
}

function subtract(a,m){
    return a-m;
}

function multiply(a, n){
    return a*n;

}

function divide(a,b){
    return a/b;
}

function increment(n){
    return n+= 1;
}

function decrement(n){
    return n-=1;
}

function makeInt(n){
    return parseInt(n, 10);
}

function preserveDecimal(n){
    return parseFloat(n, 10);
}